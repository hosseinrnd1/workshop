package model;

public class DrugsEntity {
    private String group;
    private String name;

    public String getGroup() {
        return group;
    }

    public DrugsEntity setGroup(String group) {
        this.group = group;
        return this;
    }

    public String getName() {
        return name;
    }

    public DrugsEntity setName(String name) {
        this.name = name;
        return this;
    }
}
